class Report {
  final String status;
  final String category;
  final String title;
  final String description;
  final String location;
  final String time;
  final int votes;

  Report({
    required this.status,
    required this.category,
    required this.title,
    required this.description,
    required this.location,
    required this.time,
    required this.votes,
  });
}